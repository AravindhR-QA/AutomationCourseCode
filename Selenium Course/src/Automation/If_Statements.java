package Automation;

public class If_Statements {

	public static void main(String[] args)
	{
		int a=20;
		int b=15;
		if(a>b)
		{
			System.out.println("A is Greater");
		}
		if(a<30)
		{
			System.out.println("A is Lesser than 30");
		}
		if(a>=b)
		{
			System.out.println("A is Greater or Equal to B");
		}
		if(a<=40)
		{
			System.out.println("A is Lesser or Equal to 40");
		}
		if(a==20)
		{
			System.out.println("A is Equal to 20");
		}
		if(a!=b)
		{
			System.out.println("A is NotEqual to B");
		}
	}

}
