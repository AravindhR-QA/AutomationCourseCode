package Automation;

public class ForloopContinue {

	public static void main(String[] args) 
	{
		for(int i=1;i>0;i++)
		{
			System.out.println("Infinite i++ Loop");
			System.out.println(i);
		}
		for(int i=100;i<1000;i--)
		{
			System.out.println("Infinite i-- Loop");
			System.out.println(i);
		}
	}

}
